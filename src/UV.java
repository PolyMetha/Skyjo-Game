public class UV {
    private final String name;
    private final java.awt.Color color;

    public UV(String name, java.awt.Color color){
            this.name = name;
            this.color = color;
    }

    public String getName()
    {
        return this.name;
    }

    public java.awt.Color getColor()
    {
        return this.color;
    }
}
